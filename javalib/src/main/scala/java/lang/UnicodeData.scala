  @inline private def isDirect(cp: Int): scala.Boolean =
    Integer.unsigned_<(cp, FirstRangeStart)

  private def getDataRanges(cp: Int): Int = {
    val dataRanges = this.dataRanges // local copy
    var low = 0
    var high = RangeCount * 2

    while (low + 2 != high) {
      val mid = ((low + high) >>> 1) & ~1
      if (Integer.unsigned_<(cp, dataRanges(mid)))
        high = mid
      else
        low = mid
    }

    // Fetch the data word, and apply the alternatingTypes field
    val data = dataRanges(low + 1)
    if ((data & AlternatingTypesMask) == 0) {
      data
    } else {
      // data ^ 3 if cp and startCP don't have the same parity bit
      val parityDiff = (cp ^ dataRanges(low)) & 1
      data ^ parityDiff ^ (parityDiff << 1)
    }
  }

  @inline
  private def getData(cp: Int): Int = {
    if (isDirect(cp))
      dataDirect(cp)
    else
      getDataRanges(cp)
  }

  @inline private def dataType(data: Int): Int =
    data & TypeMask

  @inline private def dataHasAnyFlag(data: Int, types: Int, flags: Int): scala.Boolean =
    (((1 << dataType(data)) & types) | (data & flags)) != 0

  @inline private def cpHasAnyFlag(cp: Int, types: Int, flags: Int): scala.Boolean =
    dataHasAnyFlag(getData(cp), types, flags)

  // Public methods (corresponding to methods in jl.Character)

  @noinline def isLowerCase(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, LowerCaseLetterType, OtherLowerCaseProp)

  @noinline def isUpperCase(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, UpperCaseLetterType, OtherUpperCaseProp)

  @noinline def isTitleCase(cp: Int): scala.Boolean =
    !isDirect(cp) && dataType(getDataRanges(cp)) == TITLECASE_LETTER

  @noinline def isDigit(cp: Int): scala.Boolean =
    if (isDirect(cp)) cp >= '0' && cp <= '9'
    else dataType(getDataRanges(cp)) == DECIMAL_DIGIT_NUMBER

  @noinline def isDefined(cp: Int): scala.Boolean =
    isDirect(cp) || dataType(getDataRanges(cp)) != UNASSIGNED

  @noinline def isLetter(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, AllLetterTypes, 0)

  @noinline def isLetterOrDigit(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, AllLetterTypes | DecimalDigitNumberType, 0)

  @noinline def isAlphabetic(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, AllLetterTypes | LetterNumberType, OtherAlphabeticProp)

  @noinline def isIdeographic(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, 0, IdeographicProp)

  private final val UnicodeIDStartTypes =
