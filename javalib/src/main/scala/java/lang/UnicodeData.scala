  private final val UnicodeIDPartTypes = {
    UnicodeIDStartTypes |
    ConnectorPunctuationType | DecimalDigitNumberType | CombiningSpacingMarkType | NonSpacingMarkType |
    FormatType // from isIdentifierIgnorable
