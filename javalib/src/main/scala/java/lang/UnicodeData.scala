  @noinline def isJavaIdentifierStart(cp: Int): scala.Boolean = {
    cpHasAnyFlag(
      cp,
      UnicodeIDStartTypes | CurrencySymbolType | ConnectorPunctuationType,
      0
    )
  }

  @noinline def isJavaIdentifierPart(cp: Int): scala.Boolean = {
    cpHasAnyFlag(
      cp,
      UnicodeIDPartTypes | CurrencySymbolType,
      NonWhitespaceISOControlProp
    )
  }

  @noinline def isUnicodeIdentifierStart(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, UnicodeIDStartTypes, OtherIDStartProp)

  @noinline def isUnicodeIdentifierPart(cp: Int): scala.Boolean = {
    cpHasAnyFlag(
      cp,
      UnicodeIDPartTypes,
      NonWhitespaceISOControlProp | OtherIDStartProp | OtherIDPartProp
    )
  }

  @noinline def isIdentifierIgnorable(cp: Int): scala.Boolean =
    cpHasAnyFlag(cp, FormatType, NonWhitespaceISOControlProp)

