package java.util.regex

import java.util.Arrays

/** Data and algorithms for simple case folding. */
private[regex] object CaseFolding {

  def caseFold(cp: Int): Int = {
    val insertionPoint = findInsertionPointInCaseFoldingFromRanges(cp)
    caseFoldCodePointBasedOnInsertionPoint(cp, insertionPoint)
  }

  def findInsertionPointInCaseFoldingFromRanges(cp: Int): Int = {
    val p0 = Arrays.binarySearch(simpleCaseFoldingFromRanges, cp)
    if (p0 >= 0) {
      // Exact start or end of range; normalize to the exact end (at odd index)
      p0 | 1
    } else {
      // Not an exact start or range; find the insertion point
      -p0 - 1
    }
  }

  def caseFoldCodePointBasedOnInsertionPoint(cp: Int, insertionPoint: Int): Int = {
    if ((insertionPoint & 1) == 0) {
      // Insertion point is even, which means it is outside of a range
      cp
    } else {
      // Insertion point is odd; it is inside the range at indices (insertionPoint-1, insertionPoint)
      val fromRangeStart = simpleCaseFoldingFromRanges(insertionPoint - 1)
      val toRangeStart = simpleCaseFoldingToStarts(insertionPoint >>> 1) // unsigned div by 2
      cp - fromRangeStart + toRangeStart
    }
  }

  def startOfCaseFoldingRange(insertionPoint: Int): Int = {
    val cpBefore = simpleCaseFoldingFromRanges(insertionPoint - 1)
    if ((insertionPoint & 1) == 0)
      cpBefore + 1 // outside a range; 1 after the end of the preceding range
    else
      cpBefore // inside a range; the start of that range
  }

  def endOfCaseFoldingRange(insertionPoint: Int): Int = {
    val cpAfter = simpleCaseFoldingFromRanges(insertionPoint)
    if ((insertionPoint & 1) == 0)
      cpAfter - 1 // outside a range; 1 before the start of the following range
    else
      cpAfter // inside a range; the end of that range
  }

